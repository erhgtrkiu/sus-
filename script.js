    async askQuestion() {
        const question = this.questionInput.value.trim();
        
        if (!question) {
            this.showError('–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –≤–æ–ø—Ä–æ—Å');
            return;
        }
        
        if (!this.currentBook) {
            this.showError('–°–Ω–∞—á–∞–ª–∞ –Ω–∞–π–¥–∏—Ç–µ –∫–Ω–∏–≥—É');
            return;
        }

        this.askBtn.disabled = true;
        this.showLoading('–ê–Ω–∞–ª–∏–∑–∏—Ä—É—é –≤–æ–ø—Ä–æ—Å...');

        try {
            // –ò–º–∏—Ç–∏—Ä—É–µ–º –æ–±—Ä–∞–±–æ—Ç–∫—É –≤–æ–ø—Ä–æ—Å–∞ AI
            await new Promise(resolve => setTimeout(resolve, 1500));
            
            const answer = this.generateAnswer(question);
            this.displayQA(question, answer);
            this.questionInput.value = '';
        } catch (error) {
            this.showError('–û—à–∏–±–∫–∞: ' + error.message);
        } finally {
            this.hideLoading();
            this.askBtn.disabled = false;
        }
    }

    generateAnswer(question) {
        const book = this.currentBook;
        const lowerQuestion = question.toLowerCase();

        // –û—Ç–≤–µ—Ç—ã –Ω–∞ —á–∞—Å—Ç—ã–µ –≤–æ–ø—Ä–æ—Å—ã
        if (lowerQuestion.includes('–∫—Ç–æ –≥–ª–∞–≤–Ω—ã–π –≥–µ—Ä–æ–π') || lowerQuestion.includes('–æ—Å–Ω–æ–≤–Ω–æ–π –ø–µ—Ä—Å–æ–Ω–∞–∂')) {
            return `–ì–ª–∞–≤–Ω—ã–π –≥–µ—Ä–æ–π –∫–Ω–∏–≥–∏ "${book.title}" - ${book.analysis.characters[0].split(' - ')[0]}. ${book.analysis.characters[0]}`;
        }

        if (lowerQuestion.includes('–æ —á—ë–º –∫–Ω–∏–≥–∞') || lowerQuestion.includes('—Å—é–∂–µ—Ç') || lowerQuestion.includes('–∫—Ä–∞—Ç–∫–æ–µ —Å–æ–¥–µ—Ä–∂–∞–Ω–∏–µ')) {
            return book.analysis.summary;
        }

        if (lowerQuestion.includes('—Ç–µ–º—ã') || lowerQuestion.includes('–∏–¥–µ–∏')) {
            return `–û—Å–Ω–æ–≤–Ω—ã–µ —Ç–µ–º—ã –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è "${book.title}":\n\n‚Ä¢ ${book.analysis.themes.join('\n‚Ä¢ ')}`;
        }

        if (lowerQuestion.includes('–ø–µ—Ä—Å–æ–Ω–∞–∂') && book.analysis.characters) {
            const characterName = this.extractCharacterName(question);
            if (characterName) {
                const character = book.analysis.characters.find(char => 
                    char.toLowerCase().includes(characterName.toLowerCase())
                );
                if (character) {
                    return character;
                }
            }
            return `–û—Å–Ω–æ–≤–Ω—ã–µ –ø–µ—Ä—Å–æ–Ω–∞–∂–∏:\n\n‚Ä¢ ${book.analysis.characters.join('\n‚Ä¢ ')}`;
        }

        if (lowerQuestion.includes('–∞–≤—Ç–æ—Ä') || lowerQuestion.includes('–∫—Ç–æ –Ω–∞–ø–∏—Å–∞–ª')) {
            return `–ö–Ω–∏–≥—É "${book.title}" –Ω–∞–ø–∏—Å–∞–ª ${book.author} –≤ ${book.year} –≥–æ–¥—É. ${book.description}`;
        }

        if (lowerQuestion.includes('–∫–æ–Ω–µ—Ü') || lowerQuestion.includes('—Ñ–∏–Ω–∞–ª') || lowerQuestion.includes('—Ä–∞–∑–≤—è–∑–∫–∞')) {
            return this.getEndingDescription(book.title);
        }

        // –û–±—â–∏–π –æ—Ç–≤–µ—Ç
        return `–ù–∞ –æ—Å–Ω–æ–≤–µ –∞–Ω–∞–ª–∏–∑–∞ –∫–Ω–∏–≥–∏ "${book.title}" –º–æ–≥—É —Å–∫–∞–∑–∞—Ç—å: ${book.analysis.summary.split('.')[0]}. –ï—Å–ª–∏ —É –≤–∞—Å –µ—Å—Ç—å –∫–æ–Ω–∫—Ä–µ—Ç–Ω—ã–π –≤–æ–ø—Ä–æ—Å –æ —Å—é–∂–µ—Ç–µ, –ø–µ—Ä—Å–æ–Ω–∞–∂–∞—Ö –∏–ª–∏ —Ç–µ–º–∞—Ö - –∑–∞–¥–∞–π—Ç–µ –µ–≥–æ –±–æ–ª–µ–µ –ø–æ–¥—Ä–æ–±–Ω–æ.`;
    }

    extractCharacterName(question) {
        const patterns = [
            /–∫—Ç–æ —Ç–∞–∫–æ–π (.+?)(?:\?|$)/i,
            /–∫—Ç–æ —Ç–∞–∫–∞—è (.+?)(?:\?|$)/i,
            /—Ä–∞—Å—Å–∫–∞–∂–∏ –æ (.+?)(?:\?|$)/i,
            /–ø–µ—Ä—Å–æ–Ω–∞–∂ (.+?)(?:\?|$)/i
        ];

        for (const pattern of patterns) {
            const match = question.match(pattern);
            if (match) {
                return match[1].trim();
            }
        }
        return null;
    }

